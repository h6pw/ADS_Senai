n1 = float(input ('digite um numero: '))
n2 = float(input ('digite outro numero: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
p = n1 ** n2
di = n1 // n2
r = n1 % n2

print ('A soma vale {}, a multiplicacao vale {}, a divisao vale {}, a potencializacao vale {}, a divisao inteira vale {} e o resto vale {}'.format(s,m,d,p,di,r))